unsigned short Modbus_CRC16(unsigned char *puchMsg, unsigned int usDataLen)
{
    unsigned char uchCRCHi = 0xFF; /* high byte of CRC initialized */
    unsigned char uchCRCLo = 0xFF; /* low byte of CRC initialized */
    unsigned uIndex;               /* will index into CRC lookup table */
    while (usDataLen--)            /* pass through message buffer */
    {
        uIndex = uchCRCLo ^ *puchMsg++; /* calculate the CRC */
        uchCRCLo = uchCRCHi ^ auchCRCHi[uIndex];
        uchCRCHi = auchCRCLo[uIndex];
    }

    return (uchCRCHi << 8 | uchCRCLo);  // 高位在左低位在右
}

void UARTDataDeal(unsigned char *data, unsigned short datalen)
{
    unsigned short crc = Modbus_CRC16(data, datalen - 2);
    if ((datalen < 3) || (crc != ((data[datalen - 2] << 8) | data[datalen - 1]))) {
        // 收到的数据校验不通过
        if (appdateflag.UpdateStartFlag) {
            appdateflag.ErrorCount++;
        }
    } else {
        switch (((data[0] << 8) | data[1] << 0)) {
        case 0x55aa:
            UpdateStart(data);
            break;
        case 0x66bb:
            GetBinData(data);
            break;
        case 0x77cc:
            UpdateEnd(data);
            break;
        case 0x88dd:
            GetCommand(data);
            break;
        default:
            break;
        }
    }
}

void UpdateStart(unsigned char *data)
{
    // 标志的处理
    memset(&appdateflag, 0, sizeof(AppUpdateFlag));
    // memset(&datagetmessage, 0, sizeof(DataGetMessage));
    memset(&appmessage, 0, sizeof(AppMessage));
    appdateflag.UpdateStartFlag = 1;

    // App信息的处理
    appmessage.AppSize = data[2] << 24 | data[3] << 16 | data[4] << 8 | data[5] << 0;
    appmessage.AppCrc = data[6] << 8 | data[7] << 0;
    appmessage.SignalPackSize = data[8] << 24 | data[9] << 16 | data[10] << 8 | data[11] << 0;
    appmessage.TotalNumber = data[12] << 8 | data[13] << 0;
    appmessage.BinFrameInformation = data[14];

    // 回报
    unsigned char send[] = {0x11, 0xee, 0x00, 0x65, 0x6c};
    Reply(send, sizeof(send));
}

